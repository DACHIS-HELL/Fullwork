function myFunction() {
  if (document.body.scrollTop > 600 || document.documentElement.scrollTop > 600) {
    document.getElementById("html").className = "bar-inner1";
    document.getElementById("Css").className = "bar-inner2";
    document.getElementById("Javascript").className = "bar-inner3";
  } 
  else {
    document.getElementById("html").className = "";
    document.getElementById("Css").className = "";
    document.getElementById("Javascript").className = "";
  }
}



